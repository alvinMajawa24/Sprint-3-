    <p> When one is interacting with public transport in kenya, especially if it's your first time, you might have a couple of questions that you need answers to.<br>
    We are going to try and give a couple of the frequently asked questions and their possible answers.</p>

    <p> 1. What are the available modes of transportation?<br>
        Answer: "The available modes of transportation typically include buses, trains, taxis, ride-sharing services like Uber or Lyft, ferries, bicycles, and walking."</p>

    <p> 2. How do I plan my journey?<br>
        Answer: "You can plan your journey using various tools such as transportation apps, official websites of transportation agencies, or by consulting paper schedules and maps available at stations or terminals." </p>
    
    <p> 3. What are the ticketing options and fares?<br>
        Answer: "Ticketing options and fares vary depending on the mode of transportation and the distance traveled. You can usually purchase tickets at stations, terminals, or online, and fares may be based on factors such as age, distance, and time of travel."</p>

    <p> 4. How can I provide feedback or file a complaint?<br>
        Answer: "You can provide feedback or file a complaint by contacting the transportation agency's customer service department through phone, email, or online forms. Many agencies also have dedicated customer service centers at stations or terminals."</p>   
        
    <p> 5. Are there any service disruptions or delays?<br>
        Answer: "Service disruptions or delays may occur due to factors such as weather conditions, mechanical issues, or scheduled maintenance work. You can stay informed about disruptions through announcements, alerts on transportation apps, or official social media channels."</p>

