    <p> The Old Nation House Station, also known as Khoja Stage at the junction of River Road and Tom Mboya Street near the Fire Station, is the city centre terminus for matatus heading to some of the Nothern suburbs of Nairobi and peri-urban sections of Nairobi that fall under Kiambu County.<br>
    It is named after the nearby building that served as the offices of the Nation Media group for decades before they moved to their current office building on Kimathi Street.</p>

